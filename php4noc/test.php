$('#submit').click(function() {
        var csv = '';
        $('select option:selected').each(function() {
                csv += $(this).text();
                csv += ',' + "\n";
        });
        csv = csv.substr(0, csv.length-1); 
        $('#csvContent').text(csv);
});
</script>
<script>
$('#submitdate').click(function() {
        var csv = '';
        var curdate = '';
        $('.datepicker').each(function() {
                curdate = $(this).datepicker('getDate');        
                csv += $.datepicker.formatDate('mm/dd/yy', curdate);
                csv += ',' + "\n";
        });
        csv = csv.substr(0, csv.length-2); 
        $('#csvContent').text(csv);
});
</script>



